<h1>about</h1>

<p>The aim of Quarantine Journal is to provide people living within this historic time period the ability to record entries into a permanent archive. <b>Because your quarantine experiences matter.</b></p>

<p>Background info: the development of this project took place during the <a href="https://en.wikipedia.org/wiki/Coronavirus" target="_blank">Coronavirus epidemic</a> and was built by <a href="https://www.alexreyes.xyz/" target="_blank">Alex Reyes.</a></p> 

<br>
<h3>How are entries permanent?</h3>

<p>Quarantine Journal uses the <a href = "https://arweave.org" target="_blank">arweave blockchain</a> to store entries, which means means all entries will be stored on the blockchain forever in the form of a permanent archive. This also means all entries are publicly viewable as well. </p>
<p>Anything written to arweave is an assertion that someone wrote this, at this time, and it hasn't been modified since. It provides a powerful way to "write history" as we know it. </p>

