<h1>about</h1>

<br>

<p>The aim of Quarantine Journal is to provide a way for people living during this historic time period the ability to record notes/reflections/etc into a permanent archive</p>

<p>Quarantine Journal uses the <a href = "https://arweave.org" target="_blank">arweave blockchain</a> to store entries, which means all entries made on this site are public and viewable by anyone. This also means all entries will be stored on the blockchain forever in the form of a permanent archive.</p>

<p>The development of this project took place during the <a href="https://en.wikipedia.org/wiki/Coronavirus" target="_blank">Coronavirus epidemic</a> and was built by <a href="https://www.alexreyes.xyz/" target="_blank">Alex Reyes.</a></p>

